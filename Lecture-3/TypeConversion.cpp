// TypeConversion.cpp
#include <iostream>
using namespace std;

int main() {

	cout << 'A' + 'A' << endl;
	cout << 'A' - 'A' << endl;

	char ch = 'A' + 3;
	cout << ch << endl;

	// Explicit Typeconversion
	cout << (char)65.11 + 1 << endl;
	cout << (char)(65.11 + 1) << endl;

	return 0;
}
















